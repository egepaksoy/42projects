int	ft_atoi(char *str)
{
	int	n;
	int	res;
	int	t;

	n = 0;
	res = 0;
	t = 1;
	while (str[n])
	{
		if (str[n] >= 48 && str[n] <= 57)
		{
			res *= 10;
			res += str[n] - 48;
		}
		if (str[n] == 45)
		{
			t *= -1;
		}
		if ((str[n] >= 65 && str[n] <= 90) || (str[n] >= 97 && str[n] <= 122))
		{
			return (res * t);
		}
		n++;
	}
	return (res * t);
}
