void	ft_rev_int_tab(int *tab, int size)
{
	int		s;
	int		f;
	int		n;
	char	c;

	s = 0;
	f = size - 1;
	while (s < f)
	{
		n = tab[s];
		tab[s] = tab[f];
		tab[f] = n;
		s++;
		f--;
	}
}
